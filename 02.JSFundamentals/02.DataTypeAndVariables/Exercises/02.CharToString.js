//Write a function which receives 3 parameters.  Each parameter is a single character.  Combine all the characters into one string and print it on the console.

//3 parameters: chars1, chars2, chars3 
//one string: one console.log

function main(chars1, chars2, chars3) {
    console.log(`${chars1}${chars2}${chars3}`);
}
main(`a`, `b`, `c`);
main(`%`,`2`, `o`);
main(`1`, `5`, `p`);



